
package proyectofinal;

class Jer {
    
    private int simple, media, compleja;
    
    public Jer (int simple, int media, int compleja) {
        
        this.simple = simple;
        this.media = media;
        this.compleja = compleja;
    }

    public void setSimple(int simple) {
        this.simple = simple;
    }

    public void setMedia(int media) {
        this.media = media;
    }

    public void setCompleja(int compleja) {
        this.compleja = compleja;
    }

    public int getSimple() {
        return simple;
    }

    public int getMedia() {
        return media;
    }

    public int getCompleja() {
        return compleja;
    }
    
}
